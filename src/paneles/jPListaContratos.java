    private void preparartabla() {
         rs=null;
         rs=con.Listar("*", "contrato", "");
         
                
            m =(DefaultTableModel) jTableContratos.getModel();
            jTableContratos.setModel(m);
            String fila[] = new String[9];
            try {
            while(rs.next()){
                fila[0]=rs.getString(1);
                fila[1]=rs.getString(3);
                rs1=con.Listar("Nombre_Cliente", "cliente", "where Id_Cliente='"+rs.getString(2)+"'");
                while(rs1.next()){
                    this.cliente=rs1.getString("Nombre_Cliente");
                }
                fila[2]=cliente;
                fila[3]=rs.getString(4);
                fila[4]=rs.getString(5);
                fila[5]=rs.getString(6);
                fila[6]=rs.getString(7);
                fila[7]=rs.getString(9);
                fila[8]=rs.getString(10);
                m.addRow(fila);
            }

        } catch (Exception ex) {
           JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
