    private void Visualizar(String Id) {
        try {
            rs=con.Listar("*", "insumos", "where (Id_Insumo="+Id+")");
            
            
            while(rs.next()){
                String codigo=rs.getString(1);
                String nombre=rs.getString(4);
                String categoria=rs.getString(3);
                String cantidad=rs.getString(6);
                String proveedor=rs.getString(2);
                String descripcion=rs.getString(5);
                String pago=rs.getString(7);
                String precio=rs.getString(8);
                
                
            rs1=con.Listar("Nom_proveedor", "proveedor", "where Id_proveedor='"+proveedor+"'");
            while(rs1.next()){
                this.nomprov=rs1.getString("Nom_proveedor");
            }
            
            jtfCodigo.setText(codigo);
            jtfNombre.setText(nombre);
            jcbCategoria.setSelectedItem(categoria);
            jtfCantidad.setText(cantidad);
            jtfProveedor.setText(nomprov);
            jtfDescripcion.setText(descripcion);
            jtfPago.setText(pago);
            jtfPrecio.setText(precio);
            }
        } catch (SQLException ex) {
            Logger.getLogger(JDCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
