    private void jbtGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtGuardarActionPerformed
        try {
            String codigo=jtfCodigo.getText();
            rs=null;
            rs=con.Listar("Id_proveedor", "proveedor", "where Nom_proveedor='"+jtfProveedor.getText()+"'");
            while(rs.next()){
                this.prov=rs.getString("Id_proveedor");
            }
            rs=con.Listar("Id_categoria_insumo", "categoria_insumo", "where Nombre_categoria='"+jcbCategoria.getSelectedItem()+"'");
            while(rs.next()){
                this.cat=rs.getString("Id_categoria_insumo");
            }
            
            if (con.actualizarRegistro("insumos", "Id_Insumo='"+jtfCodigo.getText()+"',Id_proveedor='"+prov+"',Id_Categoria_insumo='"+cat+"',Nombre_Insumo='"
                    +jtfNombre.getText()+"',Detalle_Insumo='"+jtfDescripcion.getText()+"',Cant_Insumo='"+jtfCantidad.getText()+"',Cond_Pago='"+jtfPago.getText()+"',Prec_Insumo='"
                    +jtfPrecio.getText()+"'", "where Id_Insumo='"+codigo+"'")){
                JOptionPane.showMessageDialog(rootPane, "Se han guardado los cambios", "Éxito", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(rootPane, "No se han podido guardar los cambios", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            Logger.getLogger(JDInsumo.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jbtGuardarActionPerformed

