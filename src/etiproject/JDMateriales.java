    private void jbtGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtGuardarActionPerformed
         try {
        
            rs=con.Listar("Id_proveedor", "proveedor", "where Nom_proveedor='"+jtfProveedor.getText()+"'");
            while(rs.next()){
                this.prov = rs.getString("Id_proveedor");
            }
            
            rs1=con.Listar("Id_Ubicacion", "ubicacion", "where Nombre_Ubicacion='"+jtfUbicacion.getText()+"'");
            while(rs1.next()){
                this.ub = rs1.getString("Id_Ubicacion");
            }
            
            
            if(prov.equalsIgnoreCase("")){
                JOptionPane.showMessageDialog(rootPane, "No existe tal proveedor", "Error", JOptionPane.ERROR_MESSAGE);
                jtfProveedor.requestFocus();
            }
            System.out.println(ub);
            Calendar c = new GregorianCalendar(); 
            String dia, mes, anho;
            dia = Integer.toString(c.get(Calendar.DATE));
            mes = Integer.toString(c.get(Calendar.MONTH));
            anho = Integer.toString(c.get(Calendar.YEAR));
	   
	   fecha= anho + "-" + mes +"-" + dia;
            
            if(con.actualizarRegistro("stock", "(Id_Stock='"+jtfCodigo.getText()+"',Nom_Stock='"+jtfHerramienta.getText()+"',Detalle_Stock='"
                    +jtfMedida.getText()+"',Id_proveedor='"+prov+"',Cant_Stock='"
                    +jtfCantidad.getText()+"',Cant_Actual='"+jtfCantidad.getText()+"',Marca_Stock='"+jtfMarca.getText()+"',Id_Ubicacion='"+ub+"',Categoria_Stock='"
                    +jtfCategoria.getText()+"',Precio_comp='"+jtfPrecio.getText()+"','0',Cant_Min='"
                    +jtfCantMin.getText()+"','null','Fecha_Entra_stock="+fecha+"','1999-12-31')", "where Id_Stock='"+jtfCodigo.getText()+"'")){
            JOptionPane.showMessageDialog(rootPane, "Se han guardado los cambios", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(rootPane, "No se han podido guardar los cambios", "Error", JOptionPane.ERROR_MESSAGE);
        }

        } catch (SQLException ex) {
                Logger.getLogger(JDNFactura.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jbtGuardarActionPerformed

    private void Visualizar(String Id) {
        try {
            rs=con.Listar("*", "stock", "where (Id_Stock="+Id+")");
            
            while(rs.next()){
                String codigo=rs.getString(1);
                String herramienta=rs.getString(2);
                String cant=rs.getString(5);
                String cantActual=rs.getString(6);
                String ubicacion=rs.getString(8);
                String categoria=rs.getString(9);
                String marca=rs.getString(7);
                String medida=rs.getString(3);
                String proveedor=rs.getString(4);
                String precio=rs.getString(10);
            
                rs1=con.Listar("Nombre_Ubicacion", "Ubicacion", "where Id_Ubicacion='"+ubicacion+"'");
                while(rs1.next()){
                    this.ub=rs1.getString("Nombre_Ubicacion");
                }
                rs2=con.Listar("Nom_proveedor", "proveedor", "where Id_proveedor='"+proveedor+"'");
                while(rs2.next()){
                    this.prov=rs2.getString("Nom_proveedor");
                }
                
                
            jtfCodigo.setText(codigo);
            jtfHerramienta.setText(herramienta);
            jtfCantidad.setText(cant);
            jtfCantMin.setText(cantActual);
            jtfUbicacion.setText(ub);
            jtfCategoria.setText(categoria);
            jtfMarca.setText(marca);
            jtfMedida.setText(medida);
            jtfProveedor.setText(categoria);
            jtfPrecio.setText(precio);
            }
        } catch (SQLException ex) {
            Logger.getLogger(JDCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
