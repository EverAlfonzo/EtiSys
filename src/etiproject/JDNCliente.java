    private void jbtAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtAgregarActionPerformed
        
        if(con.Altas("cliente", "", "('"+jtfRUC.getText()+"','"+jtfNombre.getText()+"','"+jtfRazon.getText()+"','"+jtfNivel.getText()+"', '"
                    +jtfDireccion.getText()+"', '"+jtfTelefono.getText()+"', '"+jtfCelular.getText()+"', '"
                    +jtfEmail.getText()+"', '"+jtfDescuento.getText()+"')")){
            JOptionPane.showMessageDialog(rootPane, "Se ha registrado al cliente", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(rootPane, "No se ha podido registrar al cliente", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jbtAgregarActionPerformed

