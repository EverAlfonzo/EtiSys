    private void jxtfBuscarCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_jxtfBuscarCaretUpdate
        
        jTableMDO.clearSelection();
        String titulos[]={"Código","ITEM","Categoría", "Detalles", "Cantidad de Minutos", "Precio por Minuto"};
        m = (new javax.swing.table.DefaultTableModel(
            new Object [][] {},
            new String [] {"Código","ITEM","Categoría", "Detalles", "Cantidad de Minutos", "Precio por Minuto"})
             {
                 boolean[] canEdit = new boolean [] {false, false, false, false, false, false,false};

                 public boolean isCellEditable(int rowIndex, int columnIndex) {
                    return canEdit [columnIndex];
                }});

        jTableMDO.setModel(m); 
        m= (DefaultTableModel) jTableMDO.getModel();
        jTableMDO.setModel(m); 
        
        
        rs=null;

        rs=con.Listar("*", "mano_de_obra", " where (Id_MdO like \"%"+ jxtfBuscar.getText()+"%\") OR (Id_Categoria like \"%"+ jxtfBuscar.getText()+"%\") "
                + "OR (Precio_MdO like \"%"+ jxtfBuscar.getText()+"%\") OR (Servicio_MdO like \"%"+ jxtfBuscar.getText()+"%\")"
                + "OR (Detalles_MdO like \"%"+ jxtfBuscar.getText()+"%\") OR (Minutos_MdO like \"%"+ jxtfBuscar.getText()+"%\")");
        
        String fila[] = new String[6];
        
        try {
            while(rs.next()){
                fila[0]=rs.getString("Id_MdO");
                fila[1]=rs.getString("Servicio_MdO");
                rs1=con.Listar("Nombre_Categoria", "Categoria_mdo", "where Id_Categoria='"+rs.getString(2)+"'");
                while(rs1.next()){
                    this.cat=rs1.getString("Nombre_Categoria");
                }
                fila[2]=cat;
                fila[3]=rs.getString("Detalles_MdO");
                fila[4]=rs.getString("Minutos_MdO");
                fila[5]=rs.getString("Precio_MdO");
                
                
                
                 m.addRow(fila);
            }
           
        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jxtfBuscarCaretUpdate

