    private void jxtfBuscarCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_jxtfBuscarCaretUpdate
        jTablePDM.clearSelection();
        String titulos[]={"Código","Fecha","Estado", "Observación", "Relacionado Anterior", "Relacionado Siguiente"};
        m = (new javax.swing.table.DefaultTableModel(
            new Object [][] {},
            new String [] {"Código","Fecha","Estado", "Observación", "Relacionado Anterior", "Relacionado Siguiente"})
             {
                 boolean[] canEdit = new boolean [] {false, false, false, false, false, false,false};

                 public boolean isCellEditable(int rowIndex, int columnIndex) {
                    return canEdit [columnIndex];
                }});

        jTablePDM.setModel(m); 
        m= (DefaultTableModel) jTablePDM.getModel();
        jTablePDM.setModel(m); 
        
        
        rs=null;

        rs=con.Listar("*", "pedido", " where (Id_pedido like \"%"+ jxtfBuscar.getText()+"%\") OR (Fecha_pedido like \"%"+ jxtfBuscar.getText()+"%\") "
                + "OR (Estado_pedido like \"%"+ jxtfBuscar.getText()+"%\") OR (Observacion like \"%"+ jxtfBuscar.getText()+"%\")"
                + "OR (Relacionado_Ant like \"%"+ jxtfBuscar.getText()+"%\") OR (Relacionado_Sig like \"%"+ jxtfBuscar.getText()+"%\")");
        
        String fila[] = new String[7];
        
        try {
            while(rs.next()){
                fila[0]=rs.getString("Id_pedido");
                fila[1]=rs.getString("Fecha_pedido");
                fila[2]=rs.getString("Estado_pedido");
                fila[3]=rs.getString("Observacion");
                fila[4]=rs.getString("Relacionado_Ant");
                fila[5]=rs.getString("Relacionado_Sig");
                
                
                
                
                 m.addRow(fila);
            }
           
        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jxtfBuscarCaretUpdate

