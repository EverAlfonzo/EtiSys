    private void jbtAprobarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtAprobarActionPerformed
        rs=null;
        int conf=JOptionPane.showConfirmDialog(rootPane, "¿Desea aprobar la Orden de Compra?", "Aprobar ODC", JOptionPane.OK_CANCEL_OPTION);
        if(conf==0){
            try {
               rs=con.Listar("Cant_pedida, Id_Stock", "detalle_pedido", "where Id_Pedido='"+jtfRelAnt.getText()+"'");
               while(rs.next()){
                   this.cant = rs.getString("Cant_pedida");
                   this.stock = rs.getString("Id_Stock");
               }
               System.out.println(stock);
               rs1=con.Listar("Cant_actual", "stock", "where Id_stock='"+stock+"'");
               while(rs1.next()){
                   this.cantActual = rs1.getString("Cant_actual");
               }

                    int cantidad=Integer.parseInt(cant);
                    int actual=Integer.parseInt(cantActual);
                    int suma=cantidad+actual;
                    String sum=Integer.toString(suma);

                    if(con.actualizarRegistro("stock", "Cant_actual='"+sum+"'", "where Id_Stock='"+stock+"'") && 
                      (con.actualizarRegistro("odc", "Estado_Orden='Aprobado'", "where Id_Orden='"+jtfCodigo.getText()+"'")) &&
                      (con.actualizarRegistro("pedido", "Estado_pedido='ODC Aprobada'", "where Id_pedido='"+jtfRelAnt.getText()+"'"))){
                        JOptionPane.showMessageDialog(rootPane, "Se ha aprobado la Orden de Compra", "Éxito", JOptionPane.INFORMATION_MESSAGE);
                        this.dispose();
                    }else{
                        JOptionPane.showMessageDialog(rootPane, "No se ha podido aprobar la Orden de Compra", "Error", JOptionPane.ERROR_MESSAGE);
                    }
           } catch (SQLException ex) {
               Logger.getLogger(JDNPDM.class.getName()).log(Level.SEVERE, null, ex);
           }
        }
    }//GEN-LAST:event_jbtAprobarActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int confir=JOptionPane.showConfirmDialog(rootPane, "¿Desea rechazar la Orden de Compra?", "Rechazar ODC", JOptionPane.OK_CANCEL_OPTION);
        if(confir==0){
            if(con.actualizarRegistro("odc", "Estado_Orden='Rechazado'", "where Id_Orden='"+jtfCodigo.getText()+"'") &&
              (con.actualizarRegistro("pedido", "Estado_pedido='ODC Rechazada'", "where Id_Pedido='"+jtfRelAnt.getText()+"'"))){
                JOptionPane.showMessageDialog(rootPane, "Se ha rechazado la Orden dde Compra", "Éxito", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(rootPane, "No se ha podido rechazar la Orden de Compra", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

