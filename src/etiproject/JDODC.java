    private void Visualizar(String Id) {
        try {
            rs=con.Listar("*", "odc", "where (Id_Orden="+Id+")");
            
            while(rs.next()){
                String codigo=rs.getString(1);
                String fecha=rs.getString(2);
                String estado=rs.getString(4);
                String rubro=rs.getString(5);
                String condicion=rs.getString(6);
                String obs=rs.getString(7);
                String relAnt=rs.getString(9);
            
            
            jtfCodigo.setText(codigo);
            jtfFecha.setText(fecha);
            jtfEstado.setText(estado);
            jtfRubro.setText(rubro);
            jtfCondicion.setText(condicion);
            jtfObservacion.setText(obs);
            jtfRelAnt.setText(relAnt);
            }
        } catch (SQLException ex) {
            Logger.getLogger(JDCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
