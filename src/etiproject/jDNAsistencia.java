    private void jBAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAgregarActionPerformed
        String Ods;
        Ods = (String) jXCBODS.getSelectedItem();

        boolean OdsVacio = false;
        if(Ods==""){
            OdsVacio=true;
        }else{
            OdsVacio=false;
        }

        boolean OdsRepetido= false;
        for(int i=0;i<jTODs.getRowCount();i++){
            if(Ods.equals(( String)jTODs.getValueAt(i, 0))){
                JOptionPane.showMessageDialog(rootPane, "Ya ha agregado esta ODS", "Error", JOptionPane.ERROR_MESSAGE);
                OdsRepetido = true;
            }
        }
        if(!OdsRepetido && !OdsVacio){
            String vec[] = new String[5];
            Integer nominal;
            if(((int) jSHInicio.getValue())%2==0){
                nominal=((int) jSHInicio.getValue());
            }else{
                nominal = ((int) jSHInicio.getValue()) -1;
            }
            vec[0]=Ods;
            vec[4]=String.valueOf(jXCBSucursal.getSelectedIndex());
            vec[3]=nominal.toString();
            vec[2]=(String) String.valueOf(jSHInicio.getValue())+":"+String.valueOf(jSMInicio.getValue());
            vec[1]= (String) String.valueOf(jSHUtil.getValue())+":"+String.valueOf(jSMUtil.getValue());

            m.addRow(vec);

        }
    }//GEN-LAST:event_jBAgregarActionPerformed

