    private void jBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuardarActionPerformed
        Time llegada = new Time((int)jSHEntrada.getValue(), (int) jSMEntrada.getValue(),0);
        Time salida = new Time((int)jSHSalida.getValue(),(int)jSMSalida.getValue(),0);
        Time extra = new Time((int) jSHExtra.getValue(),(int)jSMExtra.getValue(),0);
        boolean succes=true;
        if(con.Altas("Asistencia", "", "('"+(jXDatePicker1.getDate().getYear()+1999)+"/"+(jXDatePicker1.getDate().getMonth()+1)+"/"+(jXDatePicker1.getDate().getDate())+"','"+String.valueOf(jXCAliasUsu.getSelectedItem())+"','"
            +llegada.toString().substring(0, llegada.toString().length()-3)+"','"+salida.toString().substring(0, salida.toString().length()-3)+"',"+jSAlmuerzo.getValue()+","+extra.toString().substring(0, extra.toString().length()-3)+",'"+jXTArea.getText()+"'")){
        for(int i=0;i<jTODs.getRowCount();i++){
            if(!con.Altas("detalle_asistencia", "", "('"+(jXDatePicker1.getDate().getYear()+1999)+"/"+(jXDatePicker1.getDate().getMonth()+1)+"/"+(jXDatePicker1.getDate().getDate())+"','"+String.valueOf(jXCAliasUsu.getSelectedItem())+"',"+String.valueOf(jTODs.getValueAt(i, 4))+",'"+jTODs.getValueAt(i, 0)+"','"+jTODs.getValueAt(i, 1)+"',"+jTODs.getValueAt(i, 3)+",'"+jTODs.getValueAt(i, 2)+"')")){
                succes = false;
            }
        }
        }else{
            succes=false;
        }
        if(succes){
            JOptionPane.showMessageDialog(rootPane, "La asistencia se ha registrado con √©xito.", "√âxito", JOptionPane.INFORMATION_MESSAGE);
            if(JOptionPane.showConfirmDialog(rootPane, "¬øDesea registrar otra asistencia?","Consulta", JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION){
                m =new javax.swing.table.DefaultTableModel(
                    new Object [][] {

                    },
                    new String [] {
                        "ODS", "Hora de Inicio", "Horas Ocupadas", "Hora Nominal", "Sucursal", "√Årea"
                    }
                ) {
                    boolean[] canEdit = new boolean [] {
                        false, false, false, false, false, true
                    };

                    public boolean isCellEditable(int rowIndex, int columnIndex) {
                        return canEdit [columnIndex];
                    }
                };
                jTODs.setModel(m);
            }else{
                this.dispose();
            }
        }else{
            JOptionPane.showMessageDialog(rootPane, "Ha ocurrido un error al momento de registrar la asistencia.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jBGuardarActionPerformed

