    private void jxtfBuscarCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_jxtfBuscarCaretUpdate
        
        jTableProveedores.clearSelection();
        String titulos[]={"Código","Proveedor","Teléfono", "Dirección"};
        m = (new javax.swing.table.DefaultTableModel(
            new Object [][] {},
            new String [] {"Código","Proveedor","Teléfono", "Dirección"})
             {
                 boolean[] canEdit = new boolean [] {false, false, false, false, false, false,false};

                 public boolean isCellEditable(int rowIndex, int columnIndex) {
                    return canEdit [columnIndex];
                }});

        jTableProveedores.setModel(m); 
        m= (DefaultTableModel) jTableProveedores.getModel();
        jTableProveedores.setModel(m); 
        
        
        rs=null;

        rs=con.Listar("*", "proveedor", " where (Id_proveedor like \"%"+ jxtfBuscar.getText()+"%\") OR (Nom_proveedor like \"%"+ jxtfBuscar.getText()+"%\") "
                + "OR (Tel_proveedor like \"%"+ jxtfBuscar.getText()+"%\") OR (Dir_proveedor like \"%"+ jxtfBuscar.getText()+"%\")");
        
        String fila[] = new String[4];
        
        try {
            while(rs.next()){
                fila[0]=rs.getString("Id_proveedor");
                fila[1]=rs.getString("Nom_proveedor");
                fila[2]=rs.getString("Tel_proveedor");
                fila[3]=rs.getString("Dir_proveedor");                
                
                
                 m.addRow(fila);
            }
           
        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jxtfBuscarCaretUpdate

