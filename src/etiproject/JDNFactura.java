    private void jbtAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtAgregarActionPerformed
        try {
        
            rs=con.Listar("Id_Cliente", "cliente", "where Nombre_Cliente='"+jtfCliente.getText()+"'");
            while(rs.next()){

                    this.id = rs.getString("Id_Cliente");
            }
            
            if(id.equalsIgnoreCase("")){
                JOptionPane.showMessageDialog(rootPane, "No existe tal cliente", "Error", JOptionPane.ERROR_MESSAGE);
                jtfCliente.requestFocus();
            }
            Date fecha = jXDatePicker1.getDate();
            if (con.Altas("Factura", "" , "('"+jtfCodigo.getText()+"','"+id+"','"+user.getAlias()+"','"+(fecha.getYear()+1900)+"/"+(fecha.getMonth()+1)+"/"+fecha.getDate()+"','"
                        +jtfTipo.getText()+"','"+jtfPago.getText()+"','"+jtfRelAnt.getText()+"')")){
                JOptionPane.showMessageDialog(rootPane, "Se ha registrado la factura", "Ãxito", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(rootPane, "No se ha podido registrar la factura", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
                Logger.getLogger(JDNFactura.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }//GEN-LAST:event_jbtAgregarActionPerformed

